principle = 1000  # Principal amount
rate = 5  # Rate of interest in percentage
time = 3  # Time in years

# Calculate simple interest
simple_intrest = (principle * rate * time) / 100

# Print the result
print(f"the simple interest for a principal amount of {principle}, rate of interest {rate}%, and time period of {time} years is {simple_intrest}")

# Output:
# the simple interest for a principal amount of 1000, rate of interest 5%, and time period of 3 years is 150.0